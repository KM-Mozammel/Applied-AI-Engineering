# 04_Definition
# ===========================================================

"""
Midpoint হলো

দুটি Point-কে সংযোগকারী রেখাংশের
ঠিক মাঝখানে অবস্থিত Point।

যদি

A(x₁, y₁)

এবং

B(x₂, y₂)

হয়,

তাহলে Midpoint-এর Coordinate
Midpoint Formula দিয়ে নির্ণয় করা যায়।
"""

# ===========================================================
# 05_Formula
# ===========================================================

"""
                ( x₁ + x₂     y₁ + y₂ )
Midpoint = (  ----------- , ----------- )
                     2            2

অর্থাৎ,

প্রথমে দুইটি x-এর Average বের করো।

তারপর

দুইটি y-এর Average বের করো।

এই দুইটি Average-ই
Midpoint-এর Coordinate।
"""

# ===========================================================
# 06_Derivation (How the formula comes)
# ===========================================================

"""
ধরি,

A(2,4)

B(8,10)

আমরা এমন একটি Point M(x,y) খুঁজছি
যা A এবং B-এর ঠিক মাঝখানে।

মাঝখানে থাকার অর্থ—

x হবে দুইটি x-এর Average

অর্থাৎ,

x = (x₁ + x₂)/2

একইভাবে,

y = (y₁ + y₂)/2

সুতরাং,

               ( x₁+x₂    y₁+y₂ )
M = ( ----------- , ----------- )
                    2          2

এটাই Midpoint Formula।

এখানে কোনো জটিল গণিত নেই।

শুধু দুইটি Coordinate-এর Average নেওয়া হয়।
"""

# ===========================================================
# 07_InternalWorking / Visualization
# ===========================================================

"""
               y
               ↑

10                     ● B(8,10)
                       /
                      /
                     /
                    ● M(5,7)
                   /
                  /
                 /
4         ● A(2,4)

────────────────────────────────→ x

A থেকে B পর্যন্ত রেখার ঠিক মাঝখানে
অবস্থিত Point হলো M।

x-coordinate

(2 + 8)/2 = 5

y-coordinate

(4 + 10)/2 = 7

অতএব,

Midpoint = (5,7)
"""

# ===========================================================
# 08_Examples
# ===========================================================

print("=" * 60)
print("Example 1")
print("=" * 60)

x1, y1 = 2, 4
