"""
Coordinate Geometry-এর সবচেয়ে মৌলিক (Fundamental) ধারণা হলো Point (বিন্দু)।

একটি Point-এর কোনো দৈর্ঘ্য (Length),
প্রস্থ (Width) অথবা উচ্চতা (Height) নেই।

এটি শুধুমাত্র একটি নির্দিষ্ট অবস্থান (Location) নির্দেশ করে।

Cartesian Coordinate System-এ প্রতিটি Point-কে দুটি সংখ্যা দিয়ে প্রকাশ করা হয়।

(x, y)

যেখানে,

x = X-axis বরাবর অবস্থান

y = Y-axis বরাবর অবস্থান

উদাহরণ:

(4, 3)

মানে,

Origin থেকে

→ ডানে ৪ একক

→ উপরে ৩ একক

গেলেই Point-এ পৌঁছানো যাবে।

Coordinate Geometry-এর সবকিছু Point থেকে শুরু হয়।

কারণ—

দুটি Point মিলে একটি Line তৈরি করে।

অনেক Point মিলে Curve তৈরি করে।

বিশেষ কিছু Point মিলে Circle, Polygon, Graph ইত্যাদি তৈরি হয়।
"""


# ===========================================================
# 02_WhyThisConceptExistsInRealtionTohistory
# ===========================================================

"""
প্রাচীন জ্যামিতিতে মানুষ বিভিন্ন আকার (Shape) নিয়ে কাজ করত।

কিন্তু কোনো নির্দিষ্ট বিন্দুর অবস্থান সংখ্যার মাধ্যমে প্রকাশ করার উপায় ছিল না।

উদাহরণ:

"এই বিন্দুটি ঐ রেখার পাশে।"

এ ধরনের বর্ণনা ছিল অস্পষ্ট।

René Descartes এমন একটি ব্যবস্থা তৈরি করেন,
যেখানে প্রতিটি বিন্দুর একটি সংখ্যাগত ঠিকানা (Numerical Address) থাকবে।

ফলে,

প্রতিটি Point-এর জন্য

(x, y)

লিখলেই তার অবস্থান জানা সম্ভব হলো।

এটাই Coordinate Geometry-এর সবচেয়ে বড় পরিবর্তন।
"""


