"""
Point (বিন্দু) হলো Coordinate Plane-এর একটি নির্দিষ্ট অবস্থান।

Cartesian Coordinate System-এ

একটি Point লেখা হয়

(x, y)

যেখানে,

x = অনুভূমিক (Horizontal) অবস্থান

y = উল্লম্ব (Vertical) অবস্থান

যদি

A = (3,5)

তাহলে

A নামের Point-এর অবস্থান

x = 3

y = 5
"""


# ===========================================================
# 05_Formula
# ===========================================================

"""
Point প্রকাশের সাধারণ নিয়ম

P = (x, y)

--------------------------------

Origin

O = (0,0)

--------------------------------

X-axis-এর উপর Point

(x,0)

--------------------------------

Y-axis-এর উপর Point

(0,y)

--------------------------------

যদি

A = (x₁,y₁)

B = (x₂,y₂)

তাহলে এগুলো দুটি আলাদা Point।
"""


