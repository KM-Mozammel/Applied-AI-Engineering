# ===========================================================
# 04_Definition
# ===========================================================
"""
Definition: Fraction হলো এমন একটি সংখ্যা, যা দুটি Integer-এর অনুপাত (Ratio) হিসেবে লেখা হয়।
        a
       ---
        b
যেখানে, a = Numerator (লব); b = Denominator (হর) এবং b ≠ 0
--------------------------------
Example: 1/2, 3/5, 7/8, 15/100 সবগুলো Fraction।
"""
# ===========================================================
# 05_Formula
# ===========================================================
"""
Fraction:
      a
     ---
      b
--------------------------------
Addition: a/b + c/d = (ad + bc)
---------
bd
--------------------------------
Subtraction: a/b - c/d = (ad - bc)
---------
bd
--------------------------------
Multiplication: a/b × c/d = ac
----
bd
--------------------------------
Division: a/b ÷ c/d = a/b × d/c = ad
----
bc
"""
# ===========================================================
# 06_Derivation (How the formula comes)
# ===========================================================
"""
Example: 1/2 + 1/3
Common Denominator: 
6 -> 1/2 = 3/6 
6 -> 1/3 = 2/6 

↓ 3/6 + 2/6 = 5/6
--------------------------------
Multiplication: 2/3 × 3/5 = (2×3)/(3×5) = 6/15 = 2/5
"""
