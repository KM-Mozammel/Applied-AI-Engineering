# কোড উদাহরণ:
# একটি সহজ উদাহরণ: নতুন ডেটার সাপেক্ষে সম্ভাবনা আপডেট করা
def update_belief(prior, likelihood, evidence):
    return (likelihood * prior) / evidence

# পরীক্ষার উদাহরণ:
# prior = 0.5, likelihood = 0.8, evidence = 0.6
print(f"Updated Posterior: {update_belief(0.5, 0.8, 0.6):.4f}")