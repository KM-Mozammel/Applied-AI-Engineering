# কোড উদাহরণ:
import math

def binomial_probability(n, k, p):
    nCk = math.comb(n, k)
    return nCk * (p**k) * ((1-p)**(n-k))

# পরীক্ষার উদাহরণ:
print(f"৫ বারে ২ বার সফলতার সম্ভাবনা: {binomial_probability(5, 2, 1/6):.4f}")