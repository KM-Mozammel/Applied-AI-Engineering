"""
একটি বিচ্ছিন্ন সম্ভাবনা বিন্যাস যেখানে 'n' সংখ্যক ট্রায়ালে 'k' বার সাফল্য পাওয়ার সম্ভাবনা নির্ণয় করা হয়।
শর্তসমূহ: 
১. ট্রায়াল সংখ্যা নির্দিষ্ট (n)। 
২. প্রতিটি ট্রায়াল স্বাধীন। 
৩. সাফল্যের সম্ভাবনা (p) প্রতিবার একই থাকে।
"""

# ===========================================================
# 05_Formula
# ===========================================================
"""
P(X = k) = nCk * p^k * (1-p)^(n-k)

যেখানে:
- n: মোট ট্রায়াল সংখ্যা
- k: সফলতার সংখ্যা
- p: একবার ট্রায়ালে সফলতার সম্ভাবনা
- nCk: n থেকে k বাছাই করার উপায় (Combination)
"""

# ===========================================================
# 06_Derivation
# ===========================================================
"""
k বার সফল হওয়ার উপায় হলো nCk। প্রতিটি সফল হওয়ার সম্ভাবনা p এবং ব্যর্থ হওয়ার সম্ভাবনা 
(1-p)। যেহেতু k বার সফল এবং (n-k) বার ব্যর্থ হতে হবে, তাই তাদের গুণফল হবে:
p^k * (1-p)^(n-k)। সবশেষে nCk দিয়ে গুণ করলেই মোট সম্ভাবনা পাওয়া যায়।
"""

