"""
১. ডেটা নরমালাইজেশন (Data Normalization): মেশিন লার্নিং মডেল ট্রেইনিংয়ের আগে ডেটা 
স্কেল করতে Z-score ব্যবহার করা হয়।
২. আউটলায়ার ডিটেকশন (Anomaly Detection): গড় থেকে ৩σ দূরে থাকা ডেটাগুলোকে আউটলায়ার হিসেবে চিহ্নিত করা।
"""

# কোড উদাহরণ:
def calculate_z_score(x, mu, sigma):
    return (x - mu) / sigma

# পরীক্ষার উদাহরণ:
print(f"Z-score: {calculate_z_score(60, 50, 5):.2f}")