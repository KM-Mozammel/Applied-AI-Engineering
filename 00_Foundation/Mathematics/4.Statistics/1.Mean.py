# Mean (Average): ধরো একজন শিক্ষক জানতে চাইলেন,
# "এই ক্লাসের ছাত্ররা মোটামুটি কত নম্বর পেয়েছে?"
# প্রতিটি নম্বর আলাদা আলাদা দেখে বোঝা কঠিন। তাই জন্ম হলো Mean।
# ধারণা: সব সংখ্যার গড়। Mean= ∑x / n
    
marks = [70, 80, 90]
mean = sum(marks)/len(marks)
print(mean)

