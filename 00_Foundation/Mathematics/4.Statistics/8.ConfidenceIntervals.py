# কোড উদাহরণ:
import scipy.stats as st
import numpy as np

def get_confidence_interval(data, confidence=0.95):
    n = len(data)
    m, se = np.mean(data), st.sem(data)
    h = se * st.t.ppf((1 + confidence) / 2., n-1)
    return m-h, m+h

# পরীক্ষার উদাহরণ:
my_data = [70, 75, 80, 85, 90]
print(f"কনফিডেন্স ইন্টারভ্যাল: {get_confidence_interval(my_data)}")