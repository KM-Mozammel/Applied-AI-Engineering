# কোড উদাহরণ:
import numpy as np

def calculate_covariance(x, y):
    n = len(x)
    mean_x = sum(x) / n
    mean_y = sum(y) / n
    covariance = sum((x[i] - mean_x) * (y[i] - mean_y) for i in range(n)) / (n - 1)
    return covariance

# পরীক্ষার উদাহরণ:
x = [1, 2, 3, 4, 5]
y = [2, 4, 6, 8, 10]
print(f"কোভ্যারিয়েন্স: {calculate_covariance(x, y):.2f}")