# ===========================================================
"""
Cov(X, Y) = Σ(x_i - x̄)(y_i - ȳ) / (n - 1)

যেখানে:
- x_i, y_i: ভেরিয়েবল দুটির প্রতিটি মান
- x̄, ȳ: ভেরিয়েবল দুটির গড়
"""

