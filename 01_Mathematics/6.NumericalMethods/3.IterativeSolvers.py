# কোড উদাহরণ (নিউটন-রাফসন):
def newton_raphson(f, df, x0, tol=1e-6):
    x = x0
    while abs(f(x)) > tol:
        x = x - f(x) / df(x)
    return x

# সমাধান x^2 - 2 = 0
f = lambda x: x**2 - 2
df = lambda x: 2*x
print(f"√2 এর মান: {newton_raphson(f, df, 1):.6f}")