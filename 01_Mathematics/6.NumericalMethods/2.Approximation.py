# কোড উদাহরণ:
import math

def taylor_exp(x, terms=5):
    approx = sum((x**i) / math.factorial(i) for i in range(terms))
    return approx

print(f"e^1 এর বাস্তব মান: {math.exp(1)}")
print(f"e^1 এর অ্যাপ্রক্সিমেট মান (৫ পদ): {taylor_exp(1)}")