# ===========================================================
# 04_Definition
# ===========================================================
"""
একটি ফাংশন f(x) কে কনভেক্স বলা হয় যদি ফাংশনের ওপরের যেকোনো দুটি বিন্দুর সংযোগকারী সরলরেখা সর্বদা ফাংশনের বক্ররেখার ওপরে বা সমান অবস্থানে থাকে।
"""

# ===========================================================
# 05_Formula
# ===========================================================
"""
f(tx_1 + (1-t)x_2) ≤ tf(x_1) + (1-t)f(x_2)

যেখানে: 
- 0 ≤ t ≤ 1
- x_1, x_2 হলো ডোমেইনের দুটি বিন্দু।
"""

# ===========================================================
# 06_Derivation
# ===========================================================
"""
দ্বিতীয় অন্তরক (Second Derivative) পরীক্ষা করে কনভেক্সিটি যাচাই করা যায়। যদি f''(x) ≥ 0 হয়, তবে ফাংশনটি কনভেক্স। এটি নির্দেশ করে যে ফাংশনের ঢাল (Slope) ক্রমাগত বাড়ছে।
"""

