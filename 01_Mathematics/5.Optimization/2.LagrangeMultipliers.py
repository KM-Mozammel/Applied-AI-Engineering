# ===========================================================
# 04_Definition
# ===========================================================
"""
এটি একটি গাণিতিক কৌশল যা ল্যাগ্রাঞ্জ ফাংশন (L) তৈরির মাধ্যমে সমাধান করে:
L(x, y, λ) = f(x, y) - λ * g(x, y)
এখানে f হলো মূল ফাংশন, g হলো কনস্ট্রেইন্ট এবং λ হলো ল্যাগ্রাঞ্জ মাল্টিপ্লায়ার।
"""

# ===========================================================
# 05_Formula
# ===========================================================
"""
∇f(x, y) = λ * ∇g(x, y)
অর্থাৎ, মূল ফাংশনের গ্রেডিয়েন্ট এবং কনস্ট্রেইন্টের গ্রেডিয়েন্ট একই দিকে বা সমান্তরাল হতে হবে।
"""

# ===========================================================
# 06_Derivation
# ===========================================================
"""
আমরা যখন কোনো ফাংশনকে সীমাবদ্ধতার অধীনে সর্বোচ্চ করি, তখন সেই বিন্দুতে কনস্ট্রেইন্টের স্পর্শক (Tangent) এবং মূল ফাংশনের লেভেল কার্ভ স্পর্শ করে থাকে। এই জ্যামিতিক সত্য থেকেই সূত্রটি আসে।
"""

