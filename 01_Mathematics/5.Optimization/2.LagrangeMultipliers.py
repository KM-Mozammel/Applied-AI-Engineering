# কোড উদাহরণ (SciPy ব্যবহার করে):
from scipy.optimize import minimize

# f(x, y) = -x*y (মিনিমাইজ করার জন্য)
fun = lambda x: -x[0] * x[1]
# x+y = 10 -> x+y-10 = 0
cons = ({'type': 'eq', 'fun': lambda x: x[0] + x[1] - 10})

res = minimize(fun, [1, 1], constraints=cons)
print(f"সর্বোচ্চ মান: {-res.fun}, বিন্দু: {res.x}")