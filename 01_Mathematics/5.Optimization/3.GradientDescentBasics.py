# কোড উদাহরণ (Simple Implementation):
import numpy as np

def gradient_descent(start_x, learning_rate, iterations):
    x = start_x
    for i in range(iterations):
        gradient = 2 * x  # f(x) = x^2 এর ঢাল
        x = x - learning_rate * gradient
    return x

print(f"সর্বনিম্ন বিন্দু: {gradient_descent(5, 0.1, 50):.4f}")