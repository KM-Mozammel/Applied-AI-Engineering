# 05_Formula
# ===========================================================

"""
প্রথম সূত্র

S = n/2 × (First Term + Last Term)

অর্থাৎ,

        n
S = -------- (a + l)
        2

যেখানে,

a = First Term

l = Last Term

------------------------------------

দ্বিতীয় সূত্র

             n
S = ----------------
      2

× [2a + (n-1)d]

যেখানে,

S = Sum

a = First Term

d = Common Difference

n = Number of Terms
"""

