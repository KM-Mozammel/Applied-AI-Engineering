# 04_Definition
# ===========================================================

"""
Slope হলো

কোনো রেখার
Vertical Change
এবং
Horizontal Change-এর অনুপাত।

অর্থাৎ,

Line যত ডানে যায়,
তত উপরে বা নিচে কত যায়।

Slope-এর মান

Positive,
Negative,
Zero
অথবা
Undefined হতে পারে।
"""

# ===========================================================
# 05_Formula
# ===========================================================

"""
           Rise
Slope = --------
            Run


অথবা,

          y₂ - y₁
m = ----------------
      x₂ - x₁


যেখানে,

m = Slope

Rise = Vertical Change

Run = Horizontal Change

Δy = y₂ - y₁

Δx = x₂ - x₁
"""

# ===========================================================
# 06_Derivation (How the formula comes)
# ===========================================================

"""
ধরি,

A(2,3)

B(8,9)

Vertical Change

Δy

=

9 - 3

=

6

Horizontal Change

Δx

=

8 - 2

=

6

Slope

=

Δy / Δx

=

6 / 6

=

1

অর্থাৎ,

প্রতি ১ একক Horizontal এগোলে

১ একক Vertical উপরে উঠছে।

এভাবেই,

          y₂-y₁
m = ----------------
      x₂-x₁

সূত্রটি পাওয়া যায়।
"""

# ===========================================================
# 07_InternalWorking / Visualization
# ===========================================================

"""
                y
                ↑

9                      ● B(8,9)
                       |
                       |
                       |  Rise = 6
                       |
3          ● A(2,3)
           ───────────────────
              Run = 6

──────────────────────────────────→ x


Slope

=

Rise / Run

=

6 / 6

=

1


Slope Interpretation

m > 0

Line উপরের দিকে উঠছে।

-----------------------

m < 0

Line নিচের দিকে নামছে।

-----------------------

m = 0

Horizontal Line।

-----------------------

Run = 0

Vertical Line

Slope Undefined।
"""

