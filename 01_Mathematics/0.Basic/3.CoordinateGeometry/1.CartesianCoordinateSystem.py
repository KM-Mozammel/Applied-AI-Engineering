# ===========================================================
# 04_Definition
# ===========================================================

"""
Cartesian Coordinate System হলো এমন একটি Coordinate Framework
যেখানে দুটি পরস্পর লম্ব (Perpendicular) সংখ্যারেখা ব্যবহার করে
যেকোনো বিন্দুর অবস্থান নির্ধারণ করা হয়।

দুটি রেখা হলো—

১. X-axis (অনুভূমিক)
২. Y-axis (উল্লম্ব)

দুটি রেখা যেখানে ছেদ করে তাকে বলে—

Origin

Origin-এর Coordinate:

(0, 0)

যেকোনো Point লেখা হয়—

(x, y)

যেখানে,

x = X-axis বরাবর দূরত্ব

y = Y-axis বরাবর দূরত্ব
"""


