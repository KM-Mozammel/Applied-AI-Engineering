"""
কার্টেসিয়ান Coordinate System (Cartesian Coordinate System)
হল এমন একটি গাণিতিক পদ্ধতি যার মাধ্যমে সমতল (Plane)-এর যেকোনো
বিন্দুর (Point) সঠিক অবস্থান সংখ্যা (Numbers) ব্যবহার করে প্রকাশ করা যায়।

এই পদ্ধতিতে প্রতিটি বিন্দুকে দুটি সংখ্যা দ্বারা প্রকাশ করা হয়:

(x, y)

যেখানে,
x = Horizontal Position (বাম-ডান অবস্থান)
y = Vertical Position (উপর-নিচ অবস্থান)

উদাহরণ:

(3, 5)

মানে,
Origin থেকে
→ ডানে ৩ একক
→ উপরে ৫ একক

গেলেই সেই বিন্দুতে পৌঁছে যাব।

Coordinate Geometry-এর প্রায় সবকিছুর ভিত্তি হচ্ছে Cartesian Coordinate System।

যেমন—

✔ Distance Formula
✔ Midpoint Formula
✔ Straight Line
✔ Circle
✔ Parabola
✔ Ellipse
✔ Hyperbola
✔ Vector
✔ Machine Learning Graph
✔ Computer Graphics

সবকিছুই এর উপর নির্ভর করে।
"""


# ===========================================================
# 02_WhyThisConceptExistsInRealtionTohistory
# ===========================================================

"""
ইতিহাসে অনেক বছর আগে জ্যামিতি (Geometry) ছিল সম্পূর্ণ চিত্রনির্ভর।

মানুষ বলত—

"এই বিন্দুটি ঐ লাইনের একটু ডানে।"

অথবা

"ওই বৃত্তের মাঝামাঝি।"

এগুলো ছিল অস্পষ্ট।

কারণ কেউ নির্দিষ্টভাবে বলতে পারত না—

ঠিক কত দূরে?

ঠিক কোথায়?

১৭শ শতাব্দীতে ফরাসি গণিতবিদ

René Descartes

একটি অসাধারণ ধারণা দেন।

তিনি বলেন—

যদি প্রতিটি বিন্দুকে সংখ্যা দিয়ে প্রকাশ করা যায়,
তাহলে Geometry-কে Algebra-তে রূপান্তর করা সম্ভব।

ফলাফল—

Geometry + Algebra

=

Coordinate Geometry

এই কারণেই Cartesian Coordinate System-এর জন্ম।

"Cartesian" নামটিও Descartes-এর নাম থেকেই এসেছে।
"""


