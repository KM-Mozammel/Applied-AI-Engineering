# ===========================================================
# 09_CommonMistakes
# ===========================================================

"""
Mistake 1: Expression এবং Equation-কে এক মনে করা। x + 5 -> Expression; x + 5 = 10 -> Equation
----------------------------
Mistake 2: 2x কে 20 ধরে নেওয়া। x-এর Value না জানলে, Expression Evaluate করা যায় না।
----------------------------
Mistake 3: Expression-এ '=' যোগ করে ফেলা। x + 3 -> Expression; x + 3 = 8 -> Equation
----------------------------
Mistake 4: 2x কে x² মনে করা। Wrong: 2x = 2 × x; Correct: x² = x × x
----------------------------
Mistake 5: Variable-এর Value না বসিয়ে, Answer লিখে ফেলা।
"""

