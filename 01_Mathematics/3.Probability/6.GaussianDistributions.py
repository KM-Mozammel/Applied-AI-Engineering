# কোড উদাহরণ:
import numpy as np

# নরমাল ডিস্ট্রিবিউশন স্যাম্পল তৈরি করা
mu, sigma = 0, 1 # গড় এবং স্ট্যান্ডার্ড ডেভিয়েশন
data = np.random.normal(mu, sigma, 1000)

print(f"গড়: {np.mean(data):.4f}, স্ট্যান্ডার্ড ডেভিয়েশন: {np.std(data):.4f}")