# কোড উদাহরণ:
def conditional_probability(prob_a_and_b, prob_b):
    return prob_a_and_b / prob_b

# পরীক্ষার উদাহরণ:
# P(A∩B) = 0.2, P(B) = 0.5
print(f"Conditional Probability: {conditional_probability(0.2, 0.5):.2f}")