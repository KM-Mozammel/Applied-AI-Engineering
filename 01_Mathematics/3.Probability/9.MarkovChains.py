# Example: Weather Sunny -> Rainy -> Cloudy
# Markov Rule: Future depends only on Present

import random

states = {
    "Sunny":["Sunny","Rainy"],
    "Rainy":["Cloudy","Sunny"],
    "Cloudy":["Rainy","Sunny"]
}

current = "Sunny"

for _ in range(5):
    current = random.choice(states[current])
    print(current)
    
