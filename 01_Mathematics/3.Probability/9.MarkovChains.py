# ===========================================================
# 04_Definition
# ===========================================================
"""
একটি স্টোকাস্টিক প্রসেস (Stochastic Process) যেখানে পরবর্তী অবস্থার সম্ভাবনা শুধুমাত্র বর্তমান অবস্থার ওপর নির্ভর করে, অতীত অবস্থার ওপর নয়। একে বলা হয় 'Memoryless Property'।
"""

# ===========================================================
# 05_Formula
# ===========================================================
"""
P(X_{n+1} = s | X_n = s_n, ..., X_0 = s_0) = P(X_{n+1} = s | X_n = s_n)

যেখানে:
- X_n: বর্তমান অবস্থা
- X_{n+1}: পরবর্তী অবস্থা
- P: ট্রানজিশন প্রোবাবিলিটি ম্যাট্রিক্স (Transition Probability Matrix)
"""

# ===========================================================
# 06_Derivation
# ===========================================================
"""
এটি 'Transition Matrix' (P) এর ওপর ভিত্তি করে তৈরি। যদি একটি সিস্টেমের N সংখ্যক অবস্থা থাকে, তবে P ম্যাট্রিক্সের প্রতিটি উপাদান p_{ij} নির্দেশ করে i অবস্থা থেকে j অবস্থায় যাওয়ার সম্ভাবনা।
"""

