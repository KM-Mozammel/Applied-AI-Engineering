# Bayes Theorem: Conditional Probability থেকেমানুষ জানতে চাইল Evidence দেখে Cause বের করা যায়? উদাহরণ: Test Positive তাহলে Disease আছে? Bayes বলে Cause ← Evidence

# Formula: P(A∣B)= P(B)P(B∣A)P(A)

prior = 0.01
likelihood = 0.9
evidence = 0.05

posterior = (likelihood * prior) / evidence
print(posterior)


