# কোড উদাহরণ:
def bayes_theorem(p_b_given_a, p_a, p_b):
    return (p_b_given_a * p_a) / p_b

# পরীক্ষার উদাহরণ:
# P(A)=0.001, P(B|A)=0.99, P(B)=0.05
print(f"Posterior Probability: {bayes_theorem(0.99, 0.001, 0.05):.4f}")