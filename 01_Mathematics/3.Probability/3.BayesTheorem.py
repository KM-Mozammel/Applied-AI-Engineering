# ===========================================================
# 04_Definition
# ===========================================================
"""
বেইস উপপাদ্য একটি ঘটনার সম্ভাবনা নির্ধারণ করে যখন ঘটনাটির সাথে সম্পর্কিত অন্য কোনো 
ঘটনা ইতিমধ্যে ঘটেছে বলে জানা থাকে।
"""

# ===========================================================
# 05_Formula
# ===========================================================
"""
P(A|B) = [P(B|A) * P(A)] / P(B)

যেখানে:
- P(A|B) : Posterior (নতুন তথ্যের পর A-এর সম্ভাবনা)
- P(B|A) : Likelihood (A সত্য হলে B ঘটার সম্ভাবনা)
- P(A)   : Prior (নতুন তথ্যের আগে A-এর সম্ভাবনা)
- P(B)   : Marginal Likelihood (ঘটনা B ঘটার সামগ্রিক সম্ভাবনা)
"""

# ===========================================================
# 06_Derivation
# ===========================================================
"""
আমরা জানি:
1. P(A|B) = P(A ∩ B) / P(B)
2. P(B|A) = P(A ∩ B) / P(A)  => P(A ∩ B) = P(B|A) * P(A)

এখন (2) নং সমীকরণ থেকে P(A ∩ B)-এর মান (1) নং সমীকরণে বসিয়ে পাই:
P(A|B) = [P(B|A) * P(A)] / P(B)
"""

