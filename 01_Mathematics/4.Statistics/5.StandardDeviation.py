# কোড উদাহরণ:
import math

def calculate_std_deviation(data):
    n = len(data)
    mean = sum(data) / n
    variance = sum((x - mean) ** 2 for x in data) / n
    return math.sqrt(variance)

# পরীক্ষার উদাহরণ:
my_data = [2, 4, 4, 4, 5, 5, 7, 9]
print(f"স্ট্যান্ডার্ড ডেভিয়েশন: {calculate_std_deviation(my_data):.2f}")