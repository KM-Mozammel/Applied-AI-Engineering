# কোড উদাহরণ:
def calculate_variance(data):
    n = len(data)
    mean = sum(data) / n
    variance = sum((x - mean) ** 2 for x in data) / n
    return variance

# পরীক্ষার উদাহরণ:
my_data = [2, 4, 4, 4, 5, 5, 7, 9]
print(f"ভেদাঙ্ক: {calculate_variance(my_data):.2f}")