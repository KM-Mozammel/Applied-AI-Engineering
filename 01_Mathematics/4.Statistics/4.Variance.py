"""
ভেদাঙ্ক হলো গড় থেকে প্রতিটি ডেটা পয়েন্টের দূরত্বের বর্গের গড়। এটি নির্দেশ করে একটি ডেটাসেট তার গড় থেকে কতটা দূরে বিস্তৃত।
"""

# ===========================================================
# 05_Formula
# ===========================================================
"""
পপুলেশন ভেদাঙ্ক (σ²): Σ(x_i - μ)² / N
স্যাম্পল ভেদাঙ্ক (s²): Σ(x_i - x̄)² / (n - 1)

যেখানে:
- x_i: প্রতিটি মান
- μ বা x̄: গড়
- N বা n: মোট সংখ্যা
"""

