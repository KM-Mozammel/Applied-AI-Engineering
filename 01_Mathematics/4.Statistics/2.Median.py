# কোড উদাহরণ:
def calculate_median(data):
    sorted_data = sorted(data)
    n = len(sorted_data)
    mid = n // 2
    if n % 2 == 0:
        return (sorted_data[mid - 1] + sorted_data[mid]) / 2
    else:
        return sorted_data[mid]

# পরীক্ষার উদাহরণ:
my_data = [10, 2, 14, 8, 5, 21]
print(f"মিডিয়ান: {calculate_median(my_data)}")