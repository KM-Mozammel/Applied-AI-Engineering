# কোড উদাহরণ (Scipy ব্যবহার করে):
from scipy import stats

# স্যাম্পল ডেটা এবং পপুলেশন গড়
data = [950, 980, 990, 1000, 1010]
pop_mean = 1000

# One-sample t-test
t_stat, p_value = stats.ttest_1samp(data, pop_mean)
print(f"P-value: {p_value:.4f}")

if p_value < 0.05:
    print("Null Hypothesis বাতিল করা হয়েছে।")
else:
    print("Null Hypothesis বাতিল করা সম্ভব নয়।")