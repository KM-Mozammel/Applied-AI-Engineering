# কোড উদাহরণ:
from collections import Counter

def calculate_mode(data):
    counts = Counter(data)
    max_freq = max(counts.values())
    mode = [key for key, val in counts.items() if val == max_freq]
    return mode

# পরীক্ষার উদাহরণ:
my_data = [5, 8, 5, 3, 2, 8, 5]
print(f"মোড: {calculate_mode(my_data)}")